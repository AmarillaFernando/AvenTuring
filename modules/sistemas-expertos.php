    <h2>Sistemas que aplican conocimiento experto para decidir</h2>

    <p>
        Un Sistema Experto es un sistema de información basado en el conocimiento que actúa como consultor experto en dominios específicos, imitando el razonamiento humano para resolver problemas complejos.
    </p>

    <h3>Conceptos Principales</h3>

    <div class="concept-grid">

        <div class="concept-card">
            <h4>📘 Base de Conocimiento vs Base de Hechos</h4>
            <p>La base de conocimiento almacena reglas estáticas del experto, mientras que la base de hechos guarda los datos específicos y dinámicos de la situación.</p>
        </div>

        <div class="concept-card">
            <h4>🧩 Componentes de una Regla</h4>
            <p>Las reglas se estructuran en <strong>Premisas</strong> (antecedentes con operadores lógicos Y, O, NO) y <strong>Conclusiones</strong> que se disparan si la premisa es verdadera.</p>
        </div>

        <div class="concept-card">
            <h4>⚙️ Motor de Inferencia</h4>
            <p>Es el "cerebro" del sistema experto que relaciona reglas con hechos para obtener conclusiones lógicas similares a las humanas.</p>
        </div>

    </div>

    <div class="examples-box">
        <h3>📚 Ejemplos</h3>
        <ul class="module-list">
            <li><strong>Diagnóstico Médico:</strong> Sistemas que reciben síntomas y aplican reglas para identificar patologías.</li>
            <li><strong>Autorización de Créditos:</strong> Reglas deterministas que evalúan el balance y límite del cliente para aprobar pagos.</li>
        </ul>
    </div>

    <div class="applications-box">
        <h3>🚀 Aplicaciones Actuales</h3>
