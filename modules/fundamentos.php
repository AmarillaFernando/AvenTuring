    <h2>¿Qué es la Inteligencia Artificial?</h2>

    <p>
        La Inteligencia Artificial (IA) es el campo científico de la informática que se centra en la creación de programas y mecanismos capaces de mostrar comportamientos considerados inteligentes. Su objetivo es incrementar la capacidad de las computadoras para realizar tareas que normalmente requerirían facultades humanas como el razonamiento, el aprendizaje, la memoria, la creatividad y la toma de decisiones.
    </p>

    <h3>Conceptos Principales</h3>

    <div class="concept-grid">

        <div class="concept-card">
            <h4>🧠 IA Fuerte vs IA Débil</h4>
            <p>La IA Fuerte o General es una inteligencia capaz de igualar o superar a un ser humano en cualquier tarea intelectual. Actualmente sigue siendo una aspiración teórica.</p>
            <p>La IA Débil o Estrecha está diseñada para resolver tareas específicas, como traducir textos, reconocer imágenes o responder preguntas. Todas las aplicaciones comerciales actuales pertenecen a esta categoría.</p>
        </div>

        <div class="concept-card">
            <h4>⚖️ Los 4 Enfoques de la IA</h4>
            <p>Los sistemas inteligentes pueden clasificarse según si buscan pensar o actuar, y si intentan hacerlo como los humanos o de manera racional.</p>
            <ul>
                <li>Pensar como humanos.</li>
                <li>Actuar como humanos.</li>
                <li>Pensar racionalmente.</li>
                <li>Actuar racionalmente.</li>
            </ul>
        </div>

        <div class="concept-card">
            <h4>💬 Prueba de Turing</h4>
            <p>Propuesta por Alan Turing en 1950, sostiene que una máquina puede considerarse inteligente si una persona no logra distinguirla de otro ser humano durante una conversación.</p>
            <p>Aunque hoy existen métodos más modernos de evaluación, continúa siendo uno de los conceptos históricos más importantes de la IA.</p>
        </div>

    </div>

    <div class="examples-box">
        <h3>📚 Ejemplos Históricos</h3>
        <ul class="module-list">
            <li><strong>Deep Blue (1997):</strong> Sistema desarrollado por IBM que derrotó al campeón mundial de ajedrez Garry Kasparov.</li>
            <li><strong>ELIZA:</strong> Uno de los primeros programas capaces de procesar lenguaje natural simulando una conversación con un psicólogo.</li>
            <li><strong>Sistemas Expertos:</strong> Programas diseñados para imitar el razonamiento de especialistas humanos en dominios concretos.</li>
        </ul>
    </div>

    <div class="applications-box">
        <h3>🚀 Aplicaciones Actuales</h3>
        <ul class="module-list">
            <li><strong>Sanidad:</strong> Chatbots y asistentes médicos capaces de realizar diagnósticos preliminares a partir de síntomas.</li>
            <li><strong>Finanzas:</strong> Sistemas para detección de fraudes y análisis predictivo de mercados.</li>
            <li><strong>Logística:</strong> Optimización de rutas de transporte, distribución y vehículos autónomos.</li>
        </ul>
    </div>

