    <h2>¿Cómo entiende una IA el mundo y el lenguaje?</h2>

    <p>
        Para que una IA sea verdaderamente inteligente, debe poder entender el mundo físico. La percepción automática y el Procesamiento de Lenguaje Natural (PLN) son los puentes entre la máquina y nuestra realidad.
    </p>

    <h3>Conceptos Principales</h3>

    <div class="concept-grid">

        <div class="concept-card">
            <h4>👁️ Percepción Automática</h4>
            <p>Es la interpretación de estímulos del entorno mediante sensores. Un reto clave es la <strong>ambigüedad</strong>, ya que la máquina debe aprender que el mundo no siempre es exactamente como se percibe por ruidos o ilusiones.</p>
        </div>

        <div class="concept-card">
            <h4>💬 Procesamiento de Lenguaje Natural (PLN)</h4>
            <p>Se divide en <strong>Comprensión</strong> (entender lo que decimos) y <strong>Generación</strong> (crear narrativa humana). Utiliza vectores de palabras como Word2vec para capturar contexto y significado profundo.</p>
        </div>

        <div class="concept-card">
            <h4>🔎 Reconocimiento de Patrones</h4>
            <p>Clasifica señales como imágenes, sonidos o huellas en categorías específicas aplicando Machine Learning.</p>
        </div>

        <div class="concept-card">
            <h4>📚 Representación del Conocimiento</h4>
            <p>Organiza el saber en <strong>Tácito</strong> (personal y difícil de formalizar) y <strong>Explícito</strong> (fácil de codificar), y distingue entre <strong>Declarativo</strong> (saber qué) y <strong>Procedimental</strong> (saber cómo).</p>
        </div>

    </div>

    <div class="examples-box">
        <h3>📚 Ejemplos</h3>
        <ul class="module-list">
            <li><strong>Sistemas de Voz:</strong> Siri o Alexa, que transcriben voz a texto, procesan la intención y luego sintetizan una respuesta.</li>
            <li><strong>Biometría:</strong> Reconocimiento de iris, huellas dactilares o rostros para controles de seguridad.</li>
        </ul>
    </div>

    <div class="applications-box">
        <h3>🚀 Aplicaciones Actuales</h3>
        <ul class="module-list">
            <li><strong>Traducción Automática:</strong> Motores que analizan gramática y contexto semántico para traducir idiomas en tiempo real.</li>
            <li><strong>Diagnóstico por Imagen:</strong> Herramientas que ayudan a doctores a detectar anomalías en tomografías o electrocardiogramas.</li>
        </ul>
    </div>

    <div class="didyouknow-box">
        <h3>💡 ¿Sabías que?</h3>
        <p>El lenguaje humano es tan flexible y ambiguo que mezclar el PLN con el aprendizaje reforzado es la nueva frontera para crear chatbots capaces de debatir y persuadir.</p>
    </div>

